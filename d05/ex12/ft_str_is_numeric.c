int		ft_str_is_numeric(char *s)
{
	int i;

	i = 0;
	if (!*s)
		return (1);
	while (s[i] != '\0')
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
