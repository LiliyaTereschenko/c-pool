#include "sudoku_head.h"

void	ft_print(char **field)
{
	int i;
	int j;

	i = 0;
	while (i < SIZE)
	{
		j = 0;
		while (j < SIZE - 1)
		{
			write(1, &field[i][j], 1);
			write(1, " ", 1);
			j++;
		}
		write(1, &field[i][SIZE - 1], 1);
		write(1, "\n", 1);
		i++;
	}
}
